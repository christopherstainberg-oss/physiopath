/* =====================================================================
   PhysioPath — AI coach knowledge-base emitter

   The KB is no longer shipped pre-expanded (~11.5MB). Instead this emits a
   small browser build of the SEED + BUILDER in scripts/coach-kb-core.mjs:
   the same isomorphic code, IIFE-wrapped, that expands to window.COACH_KB at
   runtime when the Coach step opens. Result: data/coach-kb.js drops from
   ~11.5MB to ~85KB (the seed prose lives once, not once per condition ×
   template) — a ~99% cut in that asset for the same identical KB.

   Single source of truth: scripts/coach-kb-core.mjs. This file only rewrites
   its `export` into a browser IIFE; it never re-authors content.
===================================================================== */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { buildCoachKB } from "./coach-kb-core.mjs";

const HERE = dirname(fileURLToPath(import.meta.url));
const CORE = join(HERE, "coach-kb-core.mjs");
const OUT = join(HERE, "..", "data", "coach-kb.js");

/* Take the isomorphic core verbatim and turn its single ES export into a
   classic-script IIFE. loadData() injects this as a plain <script>, so it must
   assign window.COACH_KB as a side effect of executing. */
const core = readFileSync(CORE, "utf8").replace(/^export\s+/gm, "");
const built = buildCoachKB();               // build once here purely to report the entry count

const banner = `/* AUTO-GENERATED by scripts/generate-coach-kb.mjs from scripts/coach-kb-core.mjs — do not edit by hand.
   Ships the ~85KB coach-KB SEED and expands it to ${built.length} entries in the browser
   (was a ~11.5MB pre-expanded file). Educational content only. */\n`;

const emit = `${banner}(function(){\n"use strict";\n${core}\nwindow.COACH_KB = buildCoachKB();\n})();\n`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, emit);
console.log(`Wrote ${(emit.length / 1024).toFixed(1)}KB coach-kb.js (expands to ${built.length} entries in-browser) to ${OUT}`);
